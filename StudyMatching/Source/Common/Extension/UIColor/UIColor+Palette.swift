import UIKit

extension UIColor {
  enum Palette {
    static let grayLine = UIColor(hex: "#")
    
    static let naviBottomGrayLine = UIColor(hex: "#C9C9C9")
    
    static let bgColor = UIColor(hex: "#")
    
    static let placeHolderGray = UIColor(hex: "#9")
    
    static let edgeLine: UIColor = .black.withAlphaComponent(0.7)
    
    static let primary = UIColor(hex: "#")
    
    static let selectedPrimary = UIColor(hex: "#")
    
    static let searchBG = UIColor(hex: "#")
    
    static let primaryHalf = UIColor(hex: "#").withAlphaComponent(0.5)
    
    static let errorRed = UIColor(hex: "#")
    
    static let errorRedSystem: UIColor = .systemRed
  }
}
